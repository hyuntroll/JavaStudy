package Chapter_05;

public class _05_ASCII {
    public static void main(String[] args) {

        char c = 'a';
        System.out.println(c);
        System.out.println((int)c);

        c = 'b';
        System.out.println(c);
        System.out.println((int)c);
        c++;
        System.out.println(c++);
        System.out.println(c);
    }
}
