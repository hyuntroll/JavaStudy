package Chapter_04;

public class _01_SwitchCase {
    public static void main(String[] args) {
        // Switch Case

        char ranking = 14;


        switch ( ranking ) {
            case 1:
                System.out.println("I");
                break;
            case 2:
                System.out.println("love");
                break;
            default:
                System.out.println("unknown");
                break;
        }
    }
}
